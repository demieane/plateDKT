function [l23,l31,l12,y12,y31,y23,x12,x31,x23,Area,a4,a5,a6,b4,b5,b6,c4,c5,c6,d4,d5,d6,e4,e5,e6,C4,C5,C6,S4,S5,S6]=TrigElCoefsDKT(x,y,IEN)

% Calculates the coefficient b, c, ì

y23=y(IEN(2,:))-y(IEN(3,:));  % 2-3
y31=y(IEN(3,:))-y(IEN(1,:));  % 3-1
y12=y(IEN(1,:))-y(IEN(2,:));  % 1-2


x23=x(IEN(2,:))-x(IEN(3,:));% 2-3
x31=x(IEN(3,:))-x(IEN(1,:));% 3-1
x12=x(IEN(1,:))-x(IEN(2,:));% 1-2

% Length of trig sides with corresponding (s) node
l23=sqrt(x23.^2+y23.^2);
l31=sqrt(x31.^2+y31.^2);
l12=sqrt(x12.^2+y12.^2);

a4=-x23./l23.^2;
a5=-x31./l31.^2;
a6=-x12./l12.^2;

b4=3/4.*x23.*y23./l23.^2;
b5=3/4.*x31.*y31./l31.^2;
b6=3/4.*x12.*y12./l12.^2;

c4=(1/4.*x23.^2-1/2.*y23.^2)./l23.^2;
c5=(1/4.*x31.^2-1/2.*y31.^2)./l31.^2;
c6=(1/4.*x12.^2-1/2.*y12.^2)./l12.^2;

d4=-y23./l23.^2;
d5=-y31./l31.^2;
d6=-y12./l12.^2;

e4=(1/4.*y23.^2-1/2.*x23.^2)./l23.^2;
e5=(1/4.*y31.^2-1/2.*x31.^2)./l31.^2;
e6=(1/4.*y12.^2-1/2.*x12.^2)./l12.^2;

Area=1/2*(x31.*y12-x12.*y31);

C4=-y23./l23;
C5=-y31./l31;
C6=-y12./l12;

S4=x23./l23;
S5=x31./l31;
S6=x12./l12;


end



