function [ axp,ayp,bxp,byp,asp,bsp ] = Coefs2DST(k,ax,ay,bx,by,C4,C5,C6,S4,S5,S6 )
%Creates corfficients (Batoz et al)


axp=zeros(1,3);
ayp=zeros(1,3);

bxp=zeros(1,3);
byp=zeros(1,3);

asp=zeros(1,3);
bsp=zeros(1,3);

axp(1)=ax(1)+0.5*ax(5)*C5(1,k)^2+0.5*ax(6)*C6(1,k)^2+0.5*ay(5)*C5(1,k)*S5(1,k)+0.5*ay(6)*C6(1,k)*S6(1,k);
axp(2)=ax(2)+0.5*ax(4)*C4(1,k)^2+0.5*ax(6)*C6(1,k)^2+0.5*ay(4)*C4(1,k)*S4(1,k)+0.5*ay(6)*C6(1,k)*S6(1,k);
axp(3)=ax(3)+0.5*ax(4)*C4(1,k)^2+0.5*ax(5)*C5(1,k)^2+0.5*ay(4)*C4(1,k)*S4(1,k)+0.5*ay(5)*C5(1,k)*S5(1,k);

ayp(1)=ay(1)+0.5*ay(5)*S5(1,k)^2+0.5*ay(6)*S6(1,k)^2+0.5*ax(5)*C5(1,k)*S5(1,k)+0.5*ax(6)*C6(1,k)*S6(1,k);
ayp(2)=ay(2)+0.5*ay(4)*S4(1,k)^2+0.5*ay(6)*S6(1,k)^2+0.5*ax(4)*C4(1,k)*S4(1,k)+0.5*ax(6)*C6(1,k)*S6(1,k);
ayp(3)=ay(3)+0.5*ay(4)*S4(1,k)^2+0.5*ay(5)*S5(1,k)^2+0.5*ax(4)*C4(1,k)*S4(1,k)+0.5*ax(5)*C5(1,k)*S5(1,k);

bxp(1)=bx(1)+0.5*bx(5)*C5(1,k)^2+0.5*bx(6)*C6(1,k)^2+0.5*by(5)*C5(1,k)*S5(1,k)+0.5*by(6)*C6(1,k)*S6(1,k);
bxp(2)=bx(2)+0.5*bx(4)*C4(1,k)^2+0.5*bx(6)*C6(1,k)^2+0.5*by(4)*C4(1,k)*S4(1,k)+0.5*by(6)*C6(1,k)*S6(1,k);
bxp(3)=bx(3)+0.5*bx(4)*C4(1,k)^2+0.5*bx(5)*C5(1,k)^2+0.5*by(4)*C4(1,k)*S4(1,k)+0.5*by(5)*C5(1,k)*S5(1,k);

byp(1)=by(1)+0.5*by(5)*S5(1,k)^2+0.5*by(6)*S6(1,k)^2+0.5*bx(5)*C5(1,k)*S5(1,k)+0.5*bx(6)*C6(1,k)*S6(1,k);
byp(2)=by(2)+0.5*by(4)*S4(1,k)^2+0.5*by(6)*S6(1,k)^2+0.5*bx(4)*C4(1,k)*S4(1,k)+0.5*bx(6)*C6(1,k)*S6(1,k);
byp(3)=by(3)+0.5*by(4)*S4(1,k)^2+0.5*by(5)*S5(1,k)^2+0.5*bx(4)*C4(1,k)*S4(1,k)+0.5*bx(5)*C5(1,k)*S5(1,k);

asp(1)=ay(4)*S4(1,k)-ax(4)*S4(1,k);
asp(2)=ay(5)*S5(1,k)-ax(5)*S5(1,k);
asp(3)=ay(6)*S6(1,k)-ax(6)*S6(1,k);

bsp(1)=by(4)*S4(1,k)-bx(4)*S4(1,k);
bsp(2)=by(5)*S5(1,k)-bx(5)*S5(1,k);
bsp(3)=by(6)*S6(1,k)-bx(6)*S6(1,k);


end
